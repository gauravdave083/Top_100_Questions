# Check if a Number is Positive and Negative in Python
# Given an integer input, The objective is to write a code to Check if a Number is Positive or Negative in Python Language.

# For Instance,
# Input : num = 20
# Output : The number is Positive.

num = 15
if num > 0:
    print('Positive')
elif num < 0:
    print('Negative')
else:
    print('Zero')